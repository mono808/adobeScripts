if(typeof Object.prototype.create !== 'function') {
	Object.prototype.create = function(o) {
		var F = function () {}
		F.prototype = o;
		return new F();		
	};
}

Array.prototype.dim = function (dimension, initial) {
	var a = [], i;
	for (i=0; i < dimension; i += 1) {
		a[i] = initial;
	}
	return a;	
};

Array.prototype.matrix = function (m, n, initial) {
	var a, i, j, mat = [];
	for (i = 0; i < m; i += 1) {
		a = [];
		for (j = 0; j < n; j += 1) {
			a[j] = initial;
		}
		mat[i] = a;
	}
	return mat;
};

